package kr.or.kmimvc.dto;

public class Suga {

	String descSuga = "";
	String numSuga = "";
	String sumNum = "";
	
	public String getDescSuga() {
		return descSuga;
	}
	public void setDescSuga(String descSuga) {
		this.descSuga = descSuga;
	}
	public String getNumSuga() {
		return numSuga;
	}
	public void setNumSuga(String numSuga) {
		this.numSuga = numSuga;
	}
	public String getSumNum() {
		return sumNum;
	}
	public void setSumNum(String sumNum) {
		this.sumNum = sumNum;
	}
}
