package kr.or.kmimvc.dto;

public class Pumyi {

	String codDc = "";
	String numSeq = "";
	String codJujisa = "";
	String datStart = "";
	String datEnd = "";
	
	public String getCodJujisa() {
		return codJujisa;
	}
	public void setCodJujisa(String codJujisa) {
		this.codJujisa = codJujisa;
	}
	public String getCodDc() {
		return codDc;
	}
	public void setCodDc(String codDc) {
		this.codDc = codDc;
	}
	public String getNumSeq() {
		return numSeq;
	}
	public void setNumSeq(String numSeq) {
		this.numSeq = numSeq;
	}
	public String getDatStart() {
		return datStart;
	}
	public void setDatStart(String datStart) {
		this.datStart = datStart;
	}
	public String getDatEnd() {
		return datEnd;
	}
	public void setDatEnd(String datEnd) {
		this.datEnd = datEnd;
	}
	
	
}
