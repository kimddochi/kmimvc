package kr.or.kmimvc.dto;

public class Jisa {

	String codJisa = "";	//지사코드
	String descJisa = "";	//지사명
	String numSeq = "";		//몰것다뭔지


	public String getCodJisa() {
		return codJisa;
	}
	public void setCodJisa(String codJisa) {
		this.codJisa = codJisa;
	}
	public String getDescJisa() {
		return descJisa;
	}
	public void setDescJisa(String descJisa) {
		this.descJisa = descJisa;
	}
	public String getNumSeq() {
		return numSeq;
	}
	public void setNumSeq(String numSeq) {
		this.numSeq = numSeq;
	}
	
	
}
